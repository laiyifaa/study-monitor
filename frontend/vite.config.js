/**
 * ==============================================================================
 * Vite 开发服务器配置 —— 22中暑假网课学习进度监督系统
 * ==============================================================================
 * 用途：配置 Vite 开发服务器的行为，主要是 API 代理，使前端开发时
 *       能无缝调用后端 FastAPI 接口，无需处理跨域问题。
 *
 * 在部署流程中的角色：
 *   - 仅在开发环境（npm run dev）使用，生产构建（npm run build）不涉及此配置；
 *   - 生产环境由 Nginx 反向代理实现同样的效果（见 nginx/default.conf）；
 *   - 两套代理的目标一致：/api → 后端，只是实现方式不同。
 *
 * 为什么开发时需要代理而不是用 CORS：
 *   - 保持与生产环境一致的请求路径（都是 /api/*），避免开发/生产行为不一致；
 *   - 无需在后端配置 CORS 中间件，减少安全和缓存方面的副作用；
 *   - Cookie 等凭据在同域下自动携带，无需额外配置。
 * ==============================================================================
 */

import { defineConfig } from 'vite'
import vue from '@vitejs/plugin-vue'

export default defineConfig({
  plugins: [vue()],                          // Vue 3 SFC 编译插件，必须加载

  server: {
    host: '0.0.0.0',                        // 监听所有网络接口（默认只监听 localhost）
                                             // 为什么这样配：开发时可能从手机/平板等
                                             // 其他设备访问本机 IP 预览页面，
                                             // 127.0.0.1 只能本机访问，0.0.0.0 允许局域网访问
    port: 3000,                              // 开发服务器端口号
                                             // 为什么用 3000 而非默认 5173：
                                             // 与团队约定或历史项目保持一致，
                                             // 避免多人协作时端口混乱

    proxy: {
      '/api': {                              // 匹配所有 /api 开头的请求
        target: 'http://localhost:8000',     // 转发到本地后端 FastAPI 服务
                                             // 开发时前后端分别启动：
                                             //   - 后端：uvicorn 在 8000 端口
                                             //   - 前端：vite dev 在 3000 端口
        changeOrigin: true,                  // 修改代理请求的 Origin 头为目标地址
                                             // 为什么需要：部分后端框架会校验 Origin，
                                             // 不修改可能导致请求被拒绝
                                             // 注意：此处未配置 rewrite，因为后端路由
                                             // 本身就是 /api/* 前缀，无需路径重写
      },
    },
  },
})
